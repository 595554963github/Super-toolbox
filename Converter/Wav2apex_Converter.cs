        private async Task<byte[]> ConvertToMonoPcmAsync(string wavFilePath, CancellationToken cancellationToken)
        {
            var wavReader = new WaveReader();
            AudioData audioData;

            using (var wavStream = File.OpenRead(wavFilePath))
            {
                audioData = wavReader.Read(wavStream);
            }

            if (audioData == null)
            {
                throw new InvalidDataException("无法读取WAV音频数据");
            }

            var pcm16 = audioData.GetFormat<Pcm16Format>();
            if (pcm16 == null)
            {
                throw new InvalidDataException("不支持的WAV格式(仅支持PCM16)");
            }

            short[][] channels = pcm16.Channels;
            int sampleCount = pcm16.SampleCount;
            int sampleRate = pcm16.SampleRate;
            short[] mono = new short[sampleCount];

            if (channels.Length == 1)
            {
                mono = channels[0];
            }
            else
            {
                for (int i = 0; i < sampleCount; i++)
                {
                    int sum = 0;
                    for (int c = 0; c < channels.Length; c++)
                    {
                        sum += channels[c][i];
                    }
                    mono[i] = (short)(sum / channels.Length);
                }
            }

            using (var ms = new MemoryStream())
            {
                using (var writer = new BinaryWriter(ms))
                {
                    writer.Write(System.Text.Encoding.ASCII.GetBytes("RIFF"));
                    int dataSize = mono.Length * 2;
                    int fileSize = 36 + dataSize;
                    writer.Write(fileSize);
                    writer.Write(System.Text.Encoding.ASCII.GetBytes("WAVE"));
                    writer.Write(System.Text.Encoding.ASCII.GetBytes("fmt "));
                    writer.Write(16);
                    writer.Write((short)1);
                    writer.Write((short)1);
                    writer.Write(sampleRate);
                    writer.Write(sampleRate * 2);
                    writer.Write((short)2);
                    writer.Write((short)16);
                    writer.Write(System.Text.Encoding.ASCII.GetBytes("data"));
                    writer.Write(dataSize);

                    foreach (short sample in mono)
                    {
                        writer.Write(sample);
                    }
                }

                return ms.ToArray();
            }
        }

        public override async Task ExtractAsync(string directoryPath, CancellationToken cancellationToken = default)
        {
            if (!Directory.Exists(directoryPath))
            {
                ConversionError?.Invoke(this, $"源文件夹{directoryPath}不存在");
                OnConversionFailed($"源文件夹{directoryPath}不存在");
                return;
            }

            var wavFiles = Directory.GetFiles(directoryPath, "*.wav", SearchOption.AllDirectories)
                .OrderBy(f =>
                {
                    string fileName = Path.GetFileNameWithoutExtension(f);
                    var match = Regex.Match(fileName, @"_(\d+)$");
                    return match.Success && int.TryParse(match.Groups[1].Value, out int num) ? num : int.MaxValue;
                })
                .ThenBy(f => Path.GetFileNameWithoutExtension(f))
                .ToArray();

            TotalFilesToConvert = wavFiles.Length;
            int successCount = 0;

            if (wavFiles.Length == 0)
            {
                ConversionError?.Invoke(this, "未找到需要转换的WAV文件");
                OnConversionFailed("未找到需要转换的WAV文件");
                return;
            }

            ConversionStarted?.Invoke(this, $"开始转换,共{TotalFilesToConvert}个WAV文件");

            foreach (var wavFilePath in wavFiles)
            {
                cancellationToken.ThrowIfCancellationRequested();

                string fileName = Path.GetFileNameWithoutExtension(wavFilePath);
                ConversionProgress?.Invoke(this, $"正在转换:{fileName}.wav");

                string fileDirectory = Path.GetDirectoryName(wavFilePath) ?? string.Empty;
                string apexFilePath = Path.Combine(fileDirectory, $"{fileName}.apex");

                try
                {
                    byte[] monoWavData = await ConvertToMonoPcmAsync(wavFilePath, cancellationToken);

                    using (var fs = new FileStream(apexFilePath, FileMode.Create, FileAccess.Write))
                    {
                        fs.Write(APEX_HEADER, 0, APEX_HEADER.Length);
                        fs.Write(monoWavData, 0, monoWavData.Length);
                    }

                    successCount++;
                    ConversionProgress?.Invoke(this, $"已转换:{fileName}.apex");
                    OnFileConverted(apexFilePath);
                }
                catch (Exception ex)
                {
                    ConversionError?.Invoke(this, $"{fileName}.wav转换失败:{ex.Message}");
                    OnConversionFailed($"{fileName}.wav转换失败");
                }
            }

            ConversionProgress?.Invoke(this, $"转换完成,成功转换{successCount}/{TotalFilesToConvert}个文件");
            OnConversionCompleted();
        }

        public override void Extract(string directoryPath)
        {
            ExtractAsync(directoryPath).Wait();
        }
